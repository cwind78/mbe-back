package com.org.app.main.vo;

public class Point {
	private String seq_no;
	private String user_id;
	private String route;
	private String point;
	private String adjustment;
	private String event_seq_no;
	private String regist_date;
	private String regist_user_id;
	
	public String getSeq_no() {
		return seq_no;
	}
	public void setSeq_no(String seq_no) {
		this.seq_no = seq_no;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	public String getPoint() {
		return point;
	}
	public void setPoint(String point) {
		this.point = point;
	}
	public String getAdjustment() {
		return adjustment;
	}
	public void setAdjustment(String adjustment) {
		this.adjustment = adjustment;
	}
	public String getEvent_seq_no() {
		return event_seq_no;
	}
	public void setEvent_seq_no(String event_seq_no) {
		this.event_seq_no = event_seq_no;
	}
	public String getRegist_date() {
		return regist_date;
	}
	public void setRegist_date(String regist_date) {
		this.regist_date = regist_date;
	}
	public String getRegist_user_id() {
		return regist_user_id;
	}
	public void setRegist_user_id(String regist_user_id) {
		this.regist_user_id = regist_user_id;
	}
}
